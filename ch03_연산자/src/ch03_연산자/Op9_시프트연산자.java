package ch03_연산자;

public class Op9_시프트연산자 {
	
	public static void main(String[] args) {
		
		int num;
		num = 5;
		
		// <<
		/*	 	
		 			64  32  16  8  4  2  1
		 	---------------------------------
		 	num  5  0   0   0   0  1  0  1
		 	num<<1	0	0	0	1  0  1  0	--> 10  --> 5*2^1
		 	num<<2	0	0	1	0  1  0  0	--> 20  --> 5*2^2
		 	num<<3	0	1	0	1  0  0  0	--> 40	--> 5*2^3
		*/
		
		// >>
		/*	 	
					64  32  16  8  4  2  1
			---------------------------------
			num  5  0   0   0   0  1  0  1
			num>>1	0	0	0	0  0  1  0	--> 2 --> 5/2
			num>>2	0	0	0	0  0  0  1	--> 1 --> 2/2
			num>>3	0	0	0	0  0  0  0	--> 0 --> 1/2
			// 최상위 비트가 1인 경우 새로 채우는 비트도 1
		*/
		
	}
	
}
