package math;

public class Math1 {
	public static void main(String[] args) {
	
		double num = -2.78987434;
		
		System.out.println(Math.abs(num));		// 절댓값
		System.out.println(Math.ceil(num));		// 올림
		System.out.println(Math.floor(num));	// 내림
		System.out.println(Math.round(num));	// 반올림
		System.out.println(Math.sqrt(num));		// 제곱근
	}
}
